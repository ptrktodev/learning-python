# Operadores de Comparação em Python

# == Igual a
print(5 == 5)        # True
print(5 == 3)        # False

# != Diferente de
print(5 != 3)        # True
print(5 != 5)        # False

# > Maior que
print(5 > 3)         # True
print(3 > 5)         # False

# < Menor que
print(3 < 5)         # True
print(5 < 3)         # False

# >= Maior ou igual a
print(5 >= 5)        # True
print(5 >= 3)        # True
print(3 >= 5)        # False

# <= Menor ou igual a
print(5 <= 5)        # True
print(3 <= 5)        # True
print(5 <= 3)        # False

# Comparações com strings
print("abc" == "abc")      # True
print("abc" < "def")       # True (ordem alfabética)

# Comparações com variáveis
idade = 18
print(idade >= 18)         # True
print(idade == 18)         # True